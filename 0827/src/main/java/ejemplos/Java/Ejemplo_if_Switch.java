package ejemplos.Java;

public class Ejemplo_if_Switch {

	public static void main(String[] args) {
		int i = 9;
		
		switch(i) {
		
		case 0:
			System.out.println("i es 0");
			break;
		
		case 1:
			System.out.println("i es 1");
			break;
		
		case 2:
			System.out.println("i es 2");
			break;
		
		default:
			System.out.println("i es mayor que 2");
			break;
		}
	}

}
