package ejemplos.Java;

public class Ejemplo_IfElseIf {

	public static void main(String[] args) {
		int i = 100;
		
		if(i == 10) {
			System.out.println("i es igual a 10");
		}else if (i ==15) {
			System.out.println("i es igual a 15");
		}else if (i == 20) {
			System.out.println("i es igual a 20");
		}else {
			System.out.println("i no equivale a ninguna de las anteriores");
		}
	}

}
